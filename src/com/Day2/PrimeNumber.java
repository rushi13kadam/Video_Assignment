package com.Day2;

public class PrimeNumber {

	public static void main(String[] args) {

		
		int i;
		for(int num=2;num<=50;num++)
		{
			int count=0;
			for(i=2;i<i;i++)
			{
			if(num%i==0)
			{
			count++;
			}
		}
			if(count==2)
				System.out.println(i+" ");
		}
	}

}
